//Print Elements of Array

//User function template for C++
class Solution{
public:
    //Just print the space seperated array elements
	void printArray(int arr[], int n) {
	    // code here
	    for(int i=0;i<n;i++)
	    cout<<arr[i]<<" ";
	}
};